    private void validerNotesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_validerNotesActionPerformed
        // TODO add your handling code here: 
        //if code and matricule dans la table note exist, ne cree pas
        note = new Notes();
        calcN = new CalculeNotes();
        List<Notes> a;
        try {
            note.setCodes(new Cours(Integer.valueOf(idCoursLabel.getText())));
            note.setMatricule(new Etudiant(jComboBox4.getSelectedItem().toString()));
            note.setCc(Double.valueOf(jTextField11.getText()));
            note.setCcC("".equals(jTextField15.getText()) ? -1 : Double.valueOf(jTextField15.getText()));
            note.setCctrois("".equals(jTextField26.getText()) ? -1 : Double.valueOf(jTextField26.getText()));
            note.setCcquatre("".equals(jTextField27.getText()) ? -1 : Double.valueOf(jTextField27.getText()));
            note.setExamen(Double.valueOf(jTextField12.getText()));
            note.setRatrappage("".equals(jTextField13.getText()) ? -1 : Double.valueOf(jTextField13.getText()));
            note.setCct(calcN.melangerCc(note.getCc(), note.getCcC(), note.getCctrois(), note.getCcquatre(), note));
            note.setTotal(calcN.calculeTotale(note.getCct(), note.getExamen(), note.getRatrappage(), note));
            calcN.calculeCote(note.getTotal());
            note.setCote(calcN.getCote());
            note.setPoints(calcN.getPoints());
            note.setMention(calcN.getMention());
        } catch (Exception e) {
            //erreur a lentré
            universalDialogue uni = new universalDialogue(this, true);
            uni.universalDialogueText.setText("Erreur, Verifier les Entrées!!!");
            uni.setVisible(true);

        }
        NotesJpaController notes = new NotesJpaController(EISGIP_MANAGEMENTPUEntityManager0.getEntityManagerFactory());
        a = RequetesDyn.NoteDuplice(note.getMatricule().getMatricule(), note.getCodes().getCodes(), EISGIP_MANAGEMENTPUEntityManager0);

        if (!a.isEmpty()) {
           //verifier que seul le Admin peut modifier la note

            try {
                universalDialogue uni = new universalDialogue(this, true);                           
                note.setNoteId(a.get(0).getNoteId());
                notes.edit(note);
                 uni.universalDialogueText.setText("Modification De la Note Reussi");
                uni.setVisible(true);
               
            } catch (Exception e) {
                e.printStackTrace();
                universalDialogue uni = new universalDialogue(this, rootPaneCheckingEnabled);
                uni.universalDialogueText.setText("La Note N'a pas pu etre Modifier");
                uni.setVisible(true);
            }
        } else {
            try {
                notes.create(note);
                universalDialogue uni = new universalDialogue(this, rootPaneCheckingEnabled);
                uni.universalDialogueText.setText("Note Ajouté");
                uni.setVisible(true);
            } catch (Exception e) {
                universalDialogue uni = new universalDialogue(this, rootPaneCheckingEnabled);
                uni.universalDialogueText.setText("Erreur, Verifier les champs!!!");
                uni.setVisible(true);
            }
        }
    }//GEN-LAST:event_validerNotesActionPerformed

